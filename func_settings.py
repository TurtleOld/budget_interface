def get_digits_month(month):
    if month == "Январь":
        return 1
    if month == "Февраль":
        return 2
    if month == "Март":
        return 3
    if month == "Апрель":
        return 4
    if month == "Май":
        return 5
    if month == "Июнь":
        return 6
    if month == "Июль":
        return 7
    if month == "Август":
        return 8
    if month == "Сентябрь":
        return 9
    if month == "Октябрь":
        return 10
    if month == "Ноябрь":
        return 11
    if month == "Декабрь":
        return 12
